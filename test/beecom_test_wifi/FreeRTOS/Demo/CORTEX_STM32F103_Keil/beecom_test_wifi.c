sint32_t uputs(USART_TypeDef * usart, sint8_t * str)
{
	if(!usart || !str) {
		return -1;
	}
	if('\0' == *str) {
		return 0;
	}
	do {
		// 检测发送完成TC标志。
		// 为了提高函数效率，此处没有使用库函数USART_GetITStatus
		while((USART1->SR&0X40)==0)
			;
		USART_SEND(usart, *str++);
	}while('\0' != *str);
	
	return 0;
}

